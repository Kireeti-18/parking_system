const getSize = (size: string): string => {
  const formatedSize = {
    '2': 'size-2',
    '3': 'size-3',
    '4': 'size-4',
    '5': 'size-5',
    '6': 'size-6',
    '7': 'size-7',
    '8': 'size-8',
    '9': 'size-9',
    '10': 'size-10',
  }[size];

  return formatedSize || '';
};
